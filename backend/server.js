// в”Җв”Җ TLS fingerprinting via request headers в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
app.use((req, res, next) => {
  req.tls_info = {
    protocol:    req.protocol,
    headers:     req.headers,
    user_agent:  req.headers['user-agent'],
    accept_lang: req.headers['accept-language'],
    connection:  req.headers['connection'],
  };
  next();
});

// в”Җв”Җ Routes в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
